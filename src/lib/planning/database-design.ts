/**
 * MoveHQ — Supabase Database Design (PLANNING ONLY)
 *
 * This is a planning artifact rendered in MoveHQ Planning > Database Design.
 * It documents the recommended Supabase schema derived from a full review of
 * the current app (which today persists everything in localStorage / mock data).
 *
 * NOTHING here creates tables or migrations — it is a design to review before we
 * write SQL. Types/columns are *recommendations*, not the current TS shapes.
 *
 * The schema below already reflects the resolved design decisions (see
 * RESOLVED_DECISIONS at the bottom): one canonical status model, FK-based
 * assignees, a real payments table, unified calendar settings + capacity, a
 * single field catalog, one crew roster, etc.
 */

export type ColumnConstraint = "pk" | "fk" | "unique" | "required" | "optional";

export type DbColumn = {
  name: string;
  /** Suggested Postgres / Supabase type. */
  type: string;
  /** true => NOT NULL */
  required?: boolean;
  pk?: boolean;
  /** "table(column)" if this is a foreign key. */
  fk?: string;
  notes?: string;
};

export type DbEnum = {
  name: string;
  values: string[];
};

export type DbTable = {
  name: string;
  tier: "mvp" | "future";
  purpose: string;
  columns: DbColumn[];
  enums?: DbEnum[];
  indexes?: string[];
  rls?: string;
  /** UI areas / pages that read or write this table. */
  uiAreas: string[];
};

export type DbDomain = {
  id: string;
  title: string;
  summary: string;
  tables: DbTable[];
};

